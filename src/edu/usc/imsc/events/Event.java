	private String lat;
	private String lon;

	public Event(String id, String title, String summary, String location,
			String startDate, String startTime, String endDate, String endTime,
			String buildingCode, String lat, String lon) {
		this.id = id;
		this.title = title;
		this.summary = summary;
		this.location = location;
		start_date = startDate;
		start_time = startTime;
		end_date = endDate;
		end_time = endTime;
		building_code = buildingCode;
		this.lat = lat;
		this.lon = lon;
	}

