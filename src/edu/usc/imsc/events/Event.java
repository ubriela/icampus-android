	public String getStart_date() {
		return start_date;
	}

	/**
	 * @return
	 * @uml.property name="start_time"
	 */
	public String getStart_time() {
		return start_time;
	}

	/**
	 * @return
	 * @uml.property name="end_date"
	 */
	public String getEnd_date() {
		return end_date;
	}

	/**
	 * @return
	 * @uml.property name="end_time"
	 */
	public String getEnd_time() {
		return end_time;
	}

	/**
	 * @return
	 * @uml.property name="building_code"
	 */
	public String getBuilding_code() {
		return building_code;
	}

	public String getLat() {
		return lat;
	}

	public String getLon() {
		return lon;
	}
	
	

